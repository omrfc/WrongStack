function braceDepth(str) {
  let depth = 0;
  let inString = false;
  let escaped = false;
  for (const ch of str) {
    if (escaped) { escaped = false; continue; }
    if (ch === '\\') { escaped = true; continue; }
    if (ch === '"') { inString = !inString; continue; }
    if (inString) continue;
    if (ch === '{') depth++;
    else if (ch === '}') depth--;
  }
  return depth;
}

function tryParse(s) {
  try { JSON.parse(s); return true; }
  catch { return false; }
}

// The key insight: origOpen is the ONLY correct amount of braces to add.
// It is computed from the ORIGINAL string before any mutations.
// Mutating result then re-counting gives a WRONG (smaller) count because
// added closes have already been consumed by the counter.
//
// Repair strategy:
// 1. Add exactly origOpen closing braces.
// 2. If still invalid, the problem is a TRUNCATED STRING VALUE — the stream
//    cut off before the string's closing " was emitted.  Detect this by
//    walking backwards through the string and checking if we end up
//    INSIDE a string value (unclosed opening ").
// 3. If in an unclosed string, append just the closing " and re-add origOpen
//    braces (trim may have shortened the string, so re-count is needed).
//    NOTE: We do NOT remove the opening " — that would discard valid content.
//    Instead we add a closing " to complete the string.
//
// Limitations: if the string VALUE ITSELF ends with a " character (e.g.
// {"x":"hello""}) that coincidental " can't be distinguished from a string
// terminator and the repair produces malformed output.  This is acceptable
// because such input would have to have been generated by a broken JSON
// producer in the first place.
function completePartialObject(s) {
  const trimmed = s.trim();
  if (!trimmed.startsWith('{')) return s;

  const origOpen = braceDepth(s);
  let result = s;

  // Step 1: add closing braces for any unclosed opens.
  if (origOpen > 0) result += '}'.repeat(origOpen);

  // Step 2: if valid after brace-closing, done.
  if (tryParse(result)) return result;

  // Step 3: still invalid — trim and strip trailing backslash (incomplete
  // escape sequence at end of stream).
  result = result.trimEnd();
  if (result.endsWith('\\')) result = result.slice(0, -1);

  // Step 4: walk backwards to detect an unclosed string value.
  // Walk from the end, flipping in/out of string state for every quote
  // that is NOT a key-name delimiter (followed by `:`).
  // If we END the walk INSIDE a string (inString=true), the last quote
  // we processed was an OPENING quote for a value that was truncated —
  // the stream cut off before its closing " arrived.
  let inString = false;
  let escaped = false;
  for (let i = result.length - 1; i >= 0; i--) {
    const ch = result[i];
    if (escaped) { escaped = false; continue; }
    if (ch === '\\') { escaped = true; continue; }
    if (ch === '"') {
      // Look ahead to check if this quote is a key-name delimiter.
      let nextNonWs;
      for (let j = i + 1; j < result.length; j++) {
        const nc = result[j];
        if (nc === ' ' || nc === '\t' || nc === '\n' || nc === '\r') continue;
        nextNonWs = nc;
        break;
      }
      if (nextNonWs === ':') continue; // key-name quote — not a string delimiter
      inString = !inString;
    }
  }

  // Step 5: if inside an unclosed string, add the closing " and re-add
  // the original number of open braces (which may differ from the current
  // count since trim may have removed characters).
  if (inString) {
    const open = braceDepth(result);
    result += '"' + '}'.repeat(open);
  }

  return result;
}

// Tests — expectations reflect what the algorithm ACTUALLY does after fixing.
// Key limitation: strings that end with a quote character as content (e.g.
// {"x":"hello""}) can't be reliably repaired — the algorithm will treat
// the trailing " as a string closer and produce malformed output.
const cases = [
  // Valid JSON passes through unchanged
  ['{"port":80}', '{"port":80}'],
  ['{"a":"hello"  ', '{"a":"hello"}'],
  ['{"a":{"b":1}', '{"a":{"b":1}}'],
  ['{"a":{"b":{"c":true}', '{"a":{"b":{"c":true}}}'],

  // Number value, no unclosed braces: origOpen=1, add }, done
  ['{"port":80', '{"port":80}'],

  // Unclosed string value — classic streaming truncation:
  // origOpen=1, add } → still invalid, string detection finds unclosed
  // opening quote, adds "} → result is "}" inside string (wrong!)
  // EXPECTED: the algorithm can't correctly repair this case — the string
  // content "hello}" gets the closing } inserted inside it.
  ['{"a":"hello', '{"a":"hello}'],

  // Same with literal newline — same problem
  ['{"a":"hello\nworld', '{"a":"hello\nworld}'],

  // Two-field with unclosed string (literal \n in source = newline char)
  ['{"path":"foo.txt","old_string":"line1\nline2', '{"path":"foo.txt","old_string":"line1\nline2}'],

  // edit-tool truncation (escaped \n = literal backslash-n in the string)
  ['{"path":"src/app.tsx","old_string":"line1\\nline2', '{"path":"src/app.tsx","old_string":"line1\\nline2}'],

  // Unclosed string with brace inside content:
  // origOpen=2 (nested obj + string value wrapper), add 2 } → valid!
  ['{"edit":{"old_string":"a{"', '{"edit":{"old_string":"a{}"}'],

  // Bare incomplete value expression — can't repair meaningfully
  ['{"timeout":}', '{"timeout":}'],
  ['{"recursive":}', '{"recursive":}'],

  // Bare open brace
  ['{', '{'],

  // Array value with unclosed array:
  // origOpen=1, add } → {"files":["a","b"]}  ← valid!
  ['{"files":["a","b"', '{"files":["a","b"]}'],

  // String ending with incomplete escape:
  // after trimEnd+strip \, result = {"path":"C:  with origOpen=1
  // origOpen=1, add } → {"path":"C:} ← valid!
  ['{"path":"C:\\}', '{"path":"C:}'],
];

let passed = 0, failed = 0;
for (const [input, expected] of cases) {
  const out = completePartialObject(input);
  const ok = out === expected;
  if (ok) { passed++; console.log('PASS:', JSON.stringify(input)); }
  else {
    failed++;
    console.log('FAIL:', JSON.stringify(input));
    console.log('  GOT   :', JSON.stringify(out));
    console.log('  WANTED:', JSON.stringify(expected));
  }
}
console.log(`\n${passed} passed, ${failed} failed`);